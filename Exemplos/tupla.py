minha_tupla = (1, 2, 3, 2, 4, 2)


print (minha_tupla.count(2))   # devolve o número de vezes que um elemento aparece na tupla. 

print (minha_tupla.index(2, 2))   # devolve o índice da primeira aparição de um elemento na tupla. Opcionalmente, pode-se especificar o início e fim da busca. 

print(minha_tupla.index(3, 1, 4)) 

print (len(minha_tupla))   # devolve o tamanho (número de elementos) de uma tupla (ou lista, string, etc.).